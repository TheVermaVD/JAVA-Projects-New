package com.simplilearn.first.condition;

public class Loops {

	public static void main(String[] args) {
		
		int i=1;
		while(i<=5)
		{
			System.out.println(i+".ROWDY PRAKASH");
			i++;
		}
		
		do
		{
			System.out.println(i+".ROWDY PRAKASH");
			i++;
			
		}
		while(i<=10);
		
		for(i=11;i<=20;i++)
		{
			System.out.println(i+".ROWDY PRAKASH");
		
		}

	}

}
